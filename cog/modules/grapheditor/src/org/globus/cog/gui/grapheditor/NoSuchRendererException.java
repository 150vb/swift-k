package org.globus.cog.gui.grapheditor;

public class NoSuchRendererException extends RuntimeException {
	
	public NoSuchRendererException() {
		super();
	}

	public NoSuchRendererException(String message) {
		super(message);
	}

	public NoSuchRendererException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoSuchRendererException(Throwable cause) {
		super(cause);
	}

}
