package org.globus.cog.gui.util;

public class GridPosition {
	private int row, col;

	public GridPosition(int row, int col) {
		this.row = row;
		this.col = col;
	}

	public int getRow() {
		return row;
	}

	public int getCol() {
		return col;
	}
}
